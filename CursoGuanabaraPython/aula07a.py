n1 = int(input('Um valor: '))
n2 = int(input('Outro valor: '))
soma = n1 + n2
multiplicacao = n1 * n2
divisao = n1 / n2
divisaoInteira = n1 // n2
exponenciacao = n1 ** n2
print('A soma vale {} \nO produto é {} \nA divisão é {:.2f} \nA divisão inteira é {} \nA potênciação é {}'.format(soma, multiplicacao, divisao, divisaoInteira, exponenciacao))
